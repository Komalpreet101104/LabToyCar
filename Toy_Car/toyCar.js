// Constructor function to create a ToyCar object.
function ToyCar(name, color, scale, material, price, manufacturer, year, description, imageURL, extraWheels) {
    this.name = name;
    this.color = color;
    this.scale = scale;
    this.material = material;
    this.price = price;
    this.manufacturer = manufacturer;
    this.year = year;
    this.description = description;
    this.imageURL = imageURL;
    this.extraWheels = extraWheels;
}
